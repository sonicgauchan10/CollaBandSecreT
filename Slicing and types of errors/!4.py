a= int(input("Enter your marks:"))
if a > 80:
    print("Grade A")
elif a > 60 and a <= 80:
    print("Grade B")
elif a > 50 and a <= 60:
    print("Grade C")
elif a > 45 and a <= 50:
    print("Grade D")
elif a > 25 and a <= 45:
    print("Grade E")
else:
    print("Grade F")                        

                
                                    
            
        
               